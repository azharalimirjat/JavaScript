/*
Variable:
  Variables are Containers for Storing Data.

  JavaScript Variables can be declared in 4 ways:
    1. Automatically
    2. Using var
    3. Using let
    4. Using const

When to Use var, let, or const?
1. Always declare variables

2. Always use 'const' if the value should not be changed

3. Always use 'const' if the type should not be changed (Arrays and Objects)

4. Only use 'let' if you can't use const

5. Only use 'var' if you MUST support old browsers.
*/


/* Declaring Variable means just declaring variable name and not giving it value:
Syntax:
    var variableName;

EX:
*/
var name;


/* Defining variable means assigning value to the variable
Syntax:
    var VariableName = any_value;
=> If value is String type, It should be written in quotation Mark.

EX:
*/
var person = "Azhar Ali";
console.log(person) // Printing Azhar Ali.