/* 
ELSE IF STATEMENTS:

Syntax:
if (condition) {
  //  block of code to be executed if the condition is true
} 
else {
  //  block of code to be executed if the condition is false
}
*/

// Example:
var hour = 10;
if (hour < 18) {
    console.log("Good day");
} 
else {
    console.log("Good Evening");
}



// Example:
var age = 19;
if (age < 18) {
    console.log("Not Eligible");
} 
else {
    console.log("Eligible");
}