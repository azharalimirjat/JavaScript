/*
ARITHMETIC OPERATORS:
    Arithmetic operators perform arithmetic on numbers (literals or variables).

Operator	Description
+	        Addition
-	        Subtraction
*	        Multiplication
**	        Exponentiation (ES2016)
/	        Division
%	        Modulus (Remainder)
++	        Increment
--	        Decrement


Operators and Operands:
=> The numbers (in an arithmetic operation) are called operands.
=> The operation (to be performed between the two operands) is defined by an operator.

*/



/*
Adding:
    The addition operator (+) adds numbers:

Example:
*/
let x = 5;
let y = 2;
let z = x + y;
console.log("The answer of addition is: ", z)


/*
Subtracting:
    The subtraction operator (-) subtracts numbers.

Example:
*/
let a = 5;
let b = 2;
let d = a - b;
console.log("The answer of subtraction is: ", d)


/*
Multiplying:
    The multiplication operator (*) multiplies numbers.

Example:
*/
let num1 = 5;
let num2 = 2;
let result = num1 * num2;
console.log("The answer of Multiplication is: ", result)


/*
Dividing:
    The division operator (/) divides numbers.

Example:
*/
let n = 5;
let m = 2;
let res = n / m;
console.log("The answer of Division is: ", res)



/*
Remainder:
    The modulus operator (%) returns the division remainder.

Example:
*/
let n1 = 5;
let n2 = 2;
let r1 = n1 % n2;
console.log("The answer as Reminder is: ", r1)


/*
Incrementing:
    The increment operator (++) increments numbers.

Example:
*/
let number = 5;
number++;
let numb = number;
console.log("The answer after Incrementing is: ", numb)


/*
Decrementing:
    The decrement operator (--) decrements numbers.

Example:
*/
let t1 = 5;
t1--;
let t2 = t1;
console.log("The answer after Decrementing is: ", t2)