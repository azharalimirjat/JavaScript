/*
While Loop:
    The while loop loops through a block of code as long as a specified condition is true.

Syntax:
while (condition) {
  // code block to be executed
}
*/


// Example:01:
let i = 0;
while (i < 10) {
    console.log("The number is " + i);
    i++;
}



